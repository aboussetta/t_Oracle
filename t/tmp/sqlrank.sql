SELECT
  *
FROM
  (
  SELECT
    TO_CHAR ( I.SNAP_TIME , 'YYYY/MM/DD HH24:MI:SS' ) TIME ,
    E.OLD_HASH_VALUE ,
    E.EXECUTIONS -
    NVL ( S.EXECUTIONS , 0 ) EXECUTIONS ,
    E.DISK_READS -
    NVL ( S.DISK_READS , 0 ) DISK_READS ,
    E.BUFFER_GETS -
    NVL ( S.BUFFER_GETS , 0 ) BUFFER_GETS ,
    E.ROWS_PROCESSED -
    NVL ( S.ROWS_PROCESSED , 0 ) ROWS_PROCESSED ,
    ( E.CPU_TIME -
      NVL ( S.CPU_TIME , 0 ) ) /1000000 CPU_TIME ,
    ( E.ELAPSED_TIME -
      NVL ( S.ELAPSED_TIME , 0 ) ) /1000000 ELAPSED_TIME ,
    E.MODULE
  FROM
    STATS$SQL_SUMMARY E ,
    STATS$SQL_SUMMARY S ,
    STATS$SNAPSHOT I
  WHERE
    I.SNAP_ID = E.SNAP_ID
    AND &STARTSNAP_ID = S.SNAP_ID ( + )
    AND E.HASH_VALUE = S.HASH_VALUE ( + )
    AND E.DBID = S.DBID ( + )
    AND E.INSTANCE_NUMBER = S.INSTANCE_NUMBER ( + )
    AND E.SNAP_ID = &ENDSNAP_ID ORDER BY
    CPU_TIME
    DESC )
WHERE
  ROWNUM < 101

